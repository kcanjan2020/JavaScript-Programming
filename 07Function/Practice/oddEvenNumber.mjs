// Check if a number is odd or even in JavaScript
let oddEven = (num) => {
  if (num % 2 === 0) {
    console.log(`${num} is a Even Number`);
  } else {
    console.log(`${num} is a Odd Number`);
  }
};
oddEven(16); // 16 is a Even Number
oddEven(17); //17 is a Odd number
oddEven(12); // 12 is a Even Number
